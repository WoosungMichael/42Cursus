#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*start;

	start = (char *)s;
	while (*s)
		s++;
	while (start != s && c != *s)
		s--;
	if (c == *s)
		return ((char *)s);
	return (0);
}
